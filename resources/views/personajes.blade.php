<p>Monkey D. Luffy: Es el protagonista y capitán de la tripulación. Es capaz de estirarse como la goma gracias a los poderes adquiridos tras ingerir una Fruta del Diablo. Sueña con convertirse en el próximo Rey de los Piratas encontrando el One Piece.</p>
<p>Roronoa Zoro: Combatiente de la tripulación. Es un espadachín experto que lucha usando tres espadas. Sueña con convertirse en el mejor espadachín del mundo. También se caracteriza por tener un mal sentido de la orientación y discutir a menudo con Sanji.
</p>
<p>Nami: Navegante de la tripulación. Es experta en mapas y el clima, y adora el dinero. Sueña con dibujar el mayor mapa del mundo. También se irrita muy fácilmente cuando sus compañeros no se toman las cosas en serio.
</p>
<p>Usopp: Francotirador de la tripulación. Suele mostrarse cobarde y es un mentiroso compulsivo. Sueña con convertirse en un valiente guerrero, y le gustaría visitar Elbaf, hogar de los más bravos guerreros.
</p>
<p>Sanji: Cocinero de la tripulación. También es un gran luchador. Es muy enamoradizo, y trata de forma especial a las chicas de la tripulación. Sueña con encontrar el legendario mar All Blue. Suele discutir a menudo con Zoro.
</p>
<p>Tony Tony Chopper: Médico de la tripulación. Un reno capaz de caminar y hablar como los humanos gracias a los poderes adquiridos tras ingerir una Fruta del Diablo. Sueña con ser un médico capaz de curar cualquier enfermedad.
</p>
<p>Nico Robin: Arqueóloga de la tripulación. La única persona en el mundo capaz de leer los Poneglyph, y sueña con descubrir la historia del Siglo Vacío al leerlos. Puede crear nuevas partes de su cuerpo gracias a los poderes adquiridos tras ingerir una Fruta del Diablo.
</p>
<p>Franky: Carpintero de la tripulación. Es un cyborg con grandes conocimientos en tecnología. Sueña con construir un barco capaz de navegar por todo el océano, construyendo para ello el segundo barco de la tripulación, el Thousand Sunny.
</p>
<p>Brook: Músico de la tripulación. Un esqueleto viviente al que le gusta bromear. Revivió gracias a los poderes de una Fruta del Diablo que ingirió cuando aun estaba con vida. También es un habilidoso espadachín. Sueña con reencontrarse con Laboon, una ballena de la que él y su anterior tripulación fueron amigos.
</p>
<p>Jinbe: Timonel de la tripulación. Un hombre-pez que pertenecía al grupo de los Siete Guerreros del Mar. Sueña con un mundo de igualdad entre los hombres-pez y los humanos.</p>
